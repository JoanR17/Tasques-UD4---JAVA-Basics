
public class Ej1 {

	public static void main(String[] args) {

		int x = 17, y = 8;
		
		System.out.println("Suma de " + x + " + " + y + " = " + (x + y));
		System.out.println("Resta de " + x + " - " + y + " = " + (x - y));
		System.out.println("Multiplicación de " + x + " * " + y + " = " + (x * y));
		System.out.println("División de " + x + " / " + y + " = " + (x / y));
		System.out.println("Módulo de " + x + " % " + y + " = " + (x % y));

	}

}
