
public class Ej4 {

	public static void main(String[] args) {

		int n = 8;
		
		System.out.println("Valor inicial de N = " + n); //Valor inicial
		
		n += 77; // Sumamos a la variable 77
		System.out.println("N + 77 = " + n);
		
		n -= 3; // Restamos a la variable 3
		System.out.println("N - 3 = " + n);
		
		n *= 2; // Duplicamos el valor de la variable
		System.out.println("N * 2 = " + n);
		

	}

}
